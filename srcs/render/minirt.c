int minirt(char *rtfile)
{
    t_frame *rt;
    t_parse *info;
    
    rt = init_frame();
    info = init_parse();

    dialog();
    fill_frame(info, &rt, rtfile);    
    create_scene(info, rt);
    window_init(rt);
    create_objs(rt);
    render(rt);
    mlx_loop(rt->mlx_ptr);
    return(0);
}

int main(int ac, char **av)
{
    if (ac == 2)
    {
        if (filetype_is_valid(av[1], ".rt"))
            minirt(av[1]);
        return(1);
    }
    else
        write(1, "Error\n", 6);
    return (0);
}