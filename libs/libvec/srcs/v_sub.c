# include "libvec.h"

t_vec v_sub(t_vec *v1, t_vec *v2)
{
    t_vec new;

    new.x = v2->x - v1->x;
    new.y = v2->y - v1->y;
    new.z = v2->z - v1->z;
    return (new);
}