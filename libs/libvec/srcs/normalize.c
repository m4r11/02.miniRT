# include "libvec.h"

t_vec normalize(t_vec *v)
{
    t_vec p;
    p = v_3(v->x,v->y,v->z);
    float w = length(*v);
    p.x /= w;
    p.y /= w;
    p.z /= w;
    return (p);
}