#include "rtlib.h"

int		ft_atoi(char *str, int *nb)
{
	int		i;
	long	nnb;

	i = 0;
	nnb = 0;
	if (str[i] == '-')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
		nnb = 10 * nnb + (str[i++] - '0');
	if (i == 0 || (str[0] == '-' && i == 1))
		return (-1);
	*nb = (str[0] == '-') ? -nnb : nnb;
	return (i);
}